package Praktikum.PraktikumPBO.sesi5;

public class nomor2 {
    public static void main(String[] args) {
        // Looping dari 1 hingga 5
        for (int i = 1; i <= 5; i++) {
            // Menampilkan angka saat ini
            System.out.println("Iterasi ke-" + i + ": Angka = " + i);
        }
    }
}
